var num1 = 5;
var num2 = 7;

// Operador de comparação igual

if (num1 == num2){
    console.log(`${num1} é igual ${num2}`);
}

// Operador de comparação diferente

if (num1 != num2){
    console.log(`${num1} é diferente de ${num2}`);
}

// Operador de comparação maior

if (num1 > num2){
    console.log(`${num1} é maior que ${num2}`);
}

// Operador de comparação menor

if (num1 < num2){
    console.log(`${num1} é menor que ${num2}`);
}

// Operador de comparação maior ou igual

if (num1 >= num2){
    console.log(`${num1} é maior ou igual a ${num2}`);
}

// Operador de comparação menor ou igual

if (num1 <= num2){
    console.log(`${num1} é menor ou igual a ${num2}`);
}
