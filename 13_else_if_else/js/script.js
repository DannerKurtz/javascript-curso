var nome = "Matheus"

// Vamos utilizar o if, else if, else para fazer exemplos de 3 condicionais

// Se (if) nome == João
if (nome == "João"){
    console.log("Seu nome é João");
}

// Se não (else if) nome != João (caso o nome não seja o pedido)
else if(nome == "Matheus"){
    console.log("Seu nome é Matheus");
}

// Se não (else) caso não seja nenhuma das validações
else{
    console.log("Certeza que o nome está correto?");
}