var numero = "5";
// Aqui ele fara a comparação do valor e do tipo desse dado
if(numero === "5"){
    console.log("O número corresponde o valor e seu tipo de dado");
}
// Aqui ele fará a comparação apenas do valor, mesmo se o tipo do dado estiver diferente
if(numero !== "5"){
    console.log("O número se corresponde porém o tipo do dado é diferente")
}